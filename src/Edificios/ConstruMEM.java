    private void jTextFieldMunicipioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldMunicipioKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();
        if (Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "INGRESE SOLO LETRAS");
        }
    }//GEN-LAST:event_jTextFieldMunicipioKeyTyped

