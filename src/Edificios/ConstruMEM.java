    private void jButtonVerdatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerdatosActionPerformed
        // TODO add your handling code here:
        if (jTextFieldMunicipio.getText().isEmpty() || jTextFieldLargo.getText().isEmpty()
                || jTextFieldAncho.getText().isEmpty() || jTextFieldCpiso.getText().isEmpty()
                || jTextFieldApiso.getText().isEmpty() || jTextFieldArq.getText().isEmpty()) { // validacion para no continuar el pago si hay campos vacios
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS, LLENELOS PARA CONTINUAR");
        } else {
            this.llenarLista();
            this.mostrarTabla();
            this.jTabbedPane1.setSelectedIndex(2);
            jTabbedPane1.setEnabledAt(2, true);
    }//GEN-LAST:event_jButtonVerdatosActionPerformed
    }
