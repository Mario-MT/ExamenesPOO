    public void mostrarTabla() {
        this.modelo.getDataVector().removeAllElements();
        this.modelo.fireTableDataChanged();

        for (int i = 0; i < ListEd.size(); i++) {
            String[] registro = {
                this.ListEd.get(i).getTipoEd(),
                this.ListEd.get(i).getDepartament(),
                this.ListEd.get(i).getMunicipio(),
                this.ListEd.get(i).getArqui(),
                Double.toString(this.ListEd.get(i).getArea()) + " mts",
                Double.toString(this.ListEd.get(i).getPisos()),
                Double.toString(this.ListEd.get(i).getAlturaed()) + " mts"

            };
            this.modelo.addRow(registro);
        }
    }

